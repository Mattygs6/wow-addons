local function GetTooltipItemID(tt)
	local itemName,itemLink = tt:GetItem()

	if itemLink ~= nil then
		local itemString = string.match(itemLink, "item[%-?%d:]+")
		local itemId = select(2, strsplit(":", itemString))

		return itemId
	end
end

function addline_itemDKP(tt)
	local itemId = tonumber(GetTooltipItemID(tt))
	if itemId then
		local item = raid_drop_data[itemId];
		if item then
			local dkp = item["dkp"];
			local forpvp = item["forpvp"];
			if forpvp then
				tt:AddLine("|cFF00FF00 DKP |cFFF58CBA PVP: |cFFFFFFFF"..dkp)
			else
				tt:AddLine("|cFF00FF00 DKP |cFFFFFF00 Main: |cFFFFFFFF"..dkp)
			end
			tt:Show();
		end
	end
end

ItemRefTooltip:HookScript("OnTooltipSetItem", addline_itemDKP)
